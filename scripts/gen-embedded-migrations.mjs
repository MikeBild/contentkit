#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = dirname(dirname(fileURLToPath(import.meta.url)))
const migrationsDir = join(root, 'src', 'db', 'migrations')
const journalPath = join(migrationsDir, 'meta', '_journal.json')
const outputPath = join(migrationsDir, 'embedded.mjs')

const journal = JSON.parse(await readFile(journalPath, 'utf8'))
const entries = [...journal.entries].sort((a, b) => a.idx - b.idx)
const sqlByTag = {}
for (const entry of entries) {
  sqlByTag[entry.tag] = await readFile(join(migrationsDir, `${entry.tag}.sql`), 'utf8')
}

const output = `// Build-time embedded migration bundle.
//
// AUTO-GENERATED by scripts/gen-embedded-migrations.mjs. Do not edit.
// SQL and journal data are literal module values, so the Node runtime reads no
// migration files after the single Contentkit binary has been built.
import { createHash } from 'node:crypto'

export const EMBEDDED_JOURNAL = ${JSON.stringify(journal, null, 2)}
const SQL_BY_TAG = ${JSON.stringify(sqlByTag, null, 2)}

export const EMBEDDED_MIGRATIONS = EMBEDDED_JOURNAL.entries
  .slice()
  .sort((a, b) => a.idx - b.idx)
  .map((entry) => {
    const raw = SQL_BY_TAG[entry.tag]
    if (raw === undefined) throw new Error(\`embedded migration missing SQL for \${entry.tag}\`)
    const statements = raw.split('--> statement-breakpoint').map((value) => value.trim()).filter(Boolean)
    return {
      idx: entry.idx,
      tag: entry.tag,
      when: entry.when,
      hash: createHash('sha256').update(raw).digest('hex'),
      raw,
      statements,
    }
  })
`
await writeFile(outputPath, output, 'utf8')
process.stdout.write(`wrote ${relative(root, outputPath)} (${entries.length} migrations)\n`)
